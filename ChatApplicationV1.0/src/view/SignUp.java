    private void jButtonSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSignupActionPerformed
    String fullName = jTextField1.getText().trim();
    String email = jTextField2.getText().trim();
    String password = new String(jPasswordField1.getPassword()).trim();

    if (fullName.isEmpty() || email.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String passwordHash = password; 

    UserAuthManager authManager = UserAuthManager.getInstance();
    String errorMessage = authManager.signUp(fullName, email, passwordHash, "default.jpg", "Available");

    if (errorMessage == null) {
        JOptionPane.showMessageDialog(this, "Registration Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
        Login loginFrame = new Login();
        loginFrame.setVisible(true);
        loginFrame.pack();
        loginFrame.setLocationRelativeTo(null);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Registration Failed: " + errorMessage, "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonSignupActionPerformed


